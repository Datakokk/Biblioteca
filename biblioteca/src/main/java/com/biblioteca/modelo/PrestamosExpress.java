package com.biblioteca.modelo;

public class PrestamosExpress {
	private int idExpress;
	private int idPrestamo;
	private int diasPrestamos;
	
	public int getIdExpress() {
		return idExpress;
	}
	public void setIdExpress(int idExpress) {
		this.idExpress = idExpress;
	}
	public int getIdPrestamo() {
		return idPrestamo;
	}
	public void setIdPrestamo(int idPrestamo) {
		this.idPrestamo = idPrestamo;
	}
	public int getDiasPrestamos() {
		return diasPrestamos;
	}
	public void setDiasPrestamos(int diasPrestamos) {
		this.diasPrestamos = diasPrestamos;
	}
	
	
}
